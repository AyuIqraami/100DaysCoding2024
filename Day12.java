package Day12;

public class TipeData_integer {

    public static void main(String[] args) {
        //deklarasi variabel dengan tipe data int
        int data1, data2, data3;
        
        //inisialisasi
        data1 = 77;
        data2 = 13;
        data3 = 5;
        
        //mencetak nilai variabel menggunakan println
        System.out.println("Value of data1 : "+data1);
        System.out.println("Value of data2 : "+data2);
        System.out.println("Value of data3 : "+data3);
    }

}

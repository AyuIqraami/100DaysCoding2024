package fromDay25Till_THE_END;

public class Day46_OperatorLogika_Negasikebalikan {

    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;

        // operator negasi
        boolean negasiTrue = !a;
        boolean negasiFalse = !b;

        System.out.println("Negasi dari true \t: " + negasiTrue);
        System.out.println("Negasi dari false\t: " + negasiFalse);
    }
}

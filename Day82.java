package fromDay25Till_THE_END;

public class Day82_MethodCeilFloorRound {

    public static void main(String[] args) {
        double angka = 7.5;

        double hasil1 = Math.ceil(angka);
        double hasil2 = Math.floor(angka);
        long hasil3 = Math.round(angka);

        System.out.println("Hasil Method Ceil  : " + hasil1);
        System.out.println("Hasil Method Floor : " + hasil2);
        System.out.println("Hasil Method Round : " + hasil3);
    }
}

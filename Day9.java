package Day9;

public class final_variabel {

    public static void main(String[] args) {
        //final variabel merupakan variabel yang nilainya tidak dapat dirubah
        final byte biner1 = 0;
        final byte biner2 = 1;
        System.out.println("Bilangan biner : " + biner1 + " dan " + biner2);

    }
}

package Day22;

public class OperatorPerbandingan1 {

    public static void main(String[] args) {
        //Operator perbandingan : lebih besar dan lebih kecil 
        int a = 35;
        int b = 45;
        boolean hasil1 = a < b;
        boolean hasil2 = a > b;
        System.out.println(hasil1);
        System.out.println(hasil2);
    }
}
